package resource;

/**
*
*	@author Stamper - Lheureux
*/

public class ResourceFulUser<R extends Resource> {
	
	/**
	*	The current ressource.
	*/
	protected R resource;
	
	/**
	*	@return the current ressource.
	*/
	public R getResource() {
		return (this.resource);
	}
	
	/**
	*	Change the ressource to res.
	*/
	public void setResource(R res) {
		this.resource = res;
	}

	/**
	*	Set the ressource to null in order to reset it.
	*/	
	public void resetResource() {
		this.resource = null;
	}
}
